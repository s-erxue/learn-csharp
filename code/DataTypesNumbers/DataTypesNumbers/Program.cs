using System;

namespace DataTypesNumbers
{
    class Program
    {
        static void Main()
        {
            int myInt = 42;
            Console.WriteLine(myInt);

            long myLong = 2147483648L;
            Console.WriteLine(myLong);

            float pi = 3.14F;
            Console.WriteLine(pi);

            double piAsDouble = 3.14159265D;
            Console.WriteLine(piAsDouble);
        }
    }
}
